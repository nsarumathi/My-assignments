package week1.day2;

public class OddEven {
	public static void main(String[] args) {
		int a=1;
		if(a%2==0)
		{System.out.println("Even No");}
		else 
		{System.out.println("Odd No");}
		
		for(int i=1;i<=10;i++) {
			if(i%2==0)
			{System.out.println("Even No="+i);}
			else 
			{System.out.println("Odd No="+i);}
}
	}
}